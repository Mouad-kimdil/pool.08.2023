int	ft_sqrt(int x)
{
	long int	i;

	if (x <= 0)
		return (0);
	if (x == 1)
		return (1);
	i = 2;
	while (i * i <= x)
	{
		if (i * i == x)
			return (i);
		i++;
	}
	return (0);
}
